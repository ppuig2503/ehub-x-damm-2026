def write_json(path, payload: dict[str, object]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as handle:
        json.dump(payload, handle, indent=2)


def main() -> None:
    write_json(COMMODITIES_SEED_PATH, build_seed_commodities())
    write_json(SIGNALS_SEED_PATH, build_seed_signals())
    write_json(SCENARIOS_SEED_PATH, build_scenarios_catalog())
    if RUNTIME_SIGNALS_PATH.exists():
        RUNTIME_SIGNALS_PATH.unlink()
    RUNTIME_DIR.mkdir(parents=True, exist_ok=True)
    write_json(REFRESH_LOG_PATH, {"entries": []})
    print("Seed data rebuilt.")


