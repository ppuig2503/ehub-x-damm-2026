def build_proxy_series(start: date, base: float, drift: float, amplitude: float) -> list[dict[str, object]]:
    series = []
    for index in range(16):
        current = start + timedelta(days=7 * index)
        value = base + (index * drift) + math.sin(index / 2.5) * amplitude
        series.append({"date": current.isoformat(), "value": round(value, 2)})
    return series


